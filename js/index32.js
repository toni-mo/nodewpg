// -- Program Rectangle: area and perimeter --
// Defining variables
let height, base, perimeter, area, templateMessage;

height = 10;

base = 4;

perimeter = (height + base) * 2;

area = height * base;

templateMessage = `height = ${height}, base = ${base}, 
perimeter = ${perimeter}, area = ${area}.`;

// Displaying output
console.log("Rectangle, Perimeter & Area:");
console.log(templateMessage);